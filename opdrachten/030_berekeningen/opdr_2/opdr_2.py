# Waarden
c = -12
f = 102

# Van Celsius naar Fahrenheit
fahrenheit = (9/5) * c + 32

# Van Fahrenheit naar Celsius
celsius = (5/9) * (f - 32)

# Resultaten printen
print(f"{c} graden Celsius is gelijk aan {fahrenheit:.1f} graden Fahrenheit")
print(f"{f} graden Fahrenheit is gelijk aan {celsius:.1f} graden Celsius")

